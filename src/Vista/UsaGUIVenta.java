    public static int generarNumeroVenta(ArrayList<Venta> datosVentas) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        int max = 0;
        for (Venta venta : lista) {
            if (venta != null && venta.getNumero() > max) {
                max = venta.getNumero();
            }
        }
        return max + 1;
    }

    public static ArrayList<Venta> crearNuevaVenta(ArrayList<Venta> datosVentas) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        int numero = generarNumeroVenta(lista);
        Venta nueva = new Venta(numero, LocalDateTime.now(), LocalDateTime.now(), null, new ArrayList<>(), 'A');
        lista.add(nueva);
        return lista;
    }

    public static String consultarTodasVentas(ArrayList<Venta> datosVentas) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        if (lista.isEmpty()) {
            return "No hay ventas registradas.";
        }
        StringBuilder respuesta = new StringBuilder();
        for (Venta venta : lista) {
            if (venta != null) {
                respuesta.append(venta.toString()).append(System.lineSeparator());
            }
        }
        return respuesta.toString().trim();
    }

    public static String consultarVentaDadoNumero(ArrayList<Venta> datosVentas, int numeroVenta) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        for (Venta venta : lista) {
            if (venta != null && venta.getNumero() == numeroVenta) {
                return venta.toString();
            }
        }
        return "No se encontro venta con numero " + numeroVenta + ".";
    }

    public static String consultarVentaDadaPosicion(ArrayList<Venta> datosVentas, char posicionVenta) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        if (lista.isEmpty()) {
            return "No hay ventas registradas.";
        }
        char posicion = Character.toUpperCase(posicionVenta);
        if (posicion == 'P') {
            return lista.get(0).toString();
        }
        if (posicion == 'U') {
            return lista.get(lista.size() - 1).toString();
        }
        return "Posicion invalida.";
    }

    public static String consultarVentasDadoEstado(ArrayList<Venta> datosVentas, char estadoVenta) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        if (lista.isEmpty()) {
            return "No hay ventas registradas.";
        }
        StringBuilder respuesta = new StringBuilder();
        char estado = Character.toUpperCase(estadoVenta);
        for (Venta venta : lista) {
            if (venta != null && Character.toUpperCase(venta.getEstado()) == estado) {
                respuesta.append(venta.toString()).append(System.lineSeparator());
            }
        }
        if (respuesta.length() == 0) {
            return "No hay ventas para el estado indicado.";
        }
        return respuesta.toString().trim();
    }

    public static String consultarVentasDadaCategoriaPaquete(ArrayList<Venta> datosVentas, String categoriaPaquete) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        if (lista.isEmpty()) {
            return "No hay ventas registradas.";
        }
        String categoria = categoriaPaquete == null ? "" : categoriaPaquete.trim().toLowerCase();
        StringBuilder respuesta = new StringBuilder();
        for (Venta venta : lista) {
            if (venta != null && contieneCategoria(venta, categoria)) {
                respuesta.append(venta.toString()).append(System.lineSeparator());
            }
        }
        if (respuesta.length() == 0) {
            return "No hay ventas para la categoria indicada.";
        }
        return respuesta.toString().trim();
    }

    public static void generarArchivoObjetosVentas(ArrayList<Venta> datosVentas) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        Path ruta = Paths.get("ventas.dat");
        try (ObjectOutputStream salida = new ObjectOutputStream(Files.newOutputStream(ruta))) {
            salida.writeObject(lista);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "No fue posible guardar ventas: " + ex.getMessage());
        }
    }

    public static void recuperarVentasDesdeArchivoObjetos() {
        Path ruta = Paths.get("ventas.dat");
        if (!Files.exists(ruta)) {
            JOptionPane.showMessageDialog(null, "No existe archivo de ventas.");
            return;
        }
        try (ObjectInputStream entrada = new ObjectInputStream(Files.newInputStream(ruta))) {
            Object datos = entrada.readObject();
            if (datos instanceof ArrayList) {
                @SuppressWarnings("unchecked")
                ArrayList<Venta> ventas = (ArrayList<Venta>) datos;
                datosVentas = ventas;
            }
        } catch (IOException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "No fue posible recuperar ventas: " + ex.getMessage());
        }
    }

    public static void actualizarVenta(ArrayList<Venta> datosVentas, int numeroVenta, char operacion) {
        ArrayList<Venta> lista = asegurarLista(datosVentas);
        char operacionNormalizada = Character.toUpperCase(operacion);
        for (Venta venta : lista) {
            if (venta != null && venta.getNumero() == numeroVenta) {
                if (operacionNormalizada == 'C' || operacionNormalizada == 'P') {
                    venta.setEstado(operacionNormalizada);
                    venta.setFechaHoraActualizacion(LocalDateTime.now());
                }
                return;
            }
        }
    }

