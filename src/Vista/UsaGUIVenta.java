    private static ArrayList<Venta> asegurarLista(ArrayList<Venta> datos) {
        if (datos == null) {
            return new ArrayList<>();
        }
        return datos;
    }

    private static ArrayList<Cliente> asegurarListaClientes(ArrayList<Cliente> datos) {
        if (datos == null) {
            return new ArrayList<>();
        }
        return datos;
    }

    private static boolean contieneCategoria(Venta venta, String categoria) {
        if (venta == null || venta.getSusPaquetesTuristicos() == null) {
            return false;
        }
        for (PaqueteTuristico paquete : venta.getSusPaquetesTuristicos()) {
            if (paquete == null) {
                continue;
            }
            if ("unico".equals(categoria) && paquete.getClass().getSimpleName().equals("PaqueteTuristicoUnico")) {
                return true;
            }
            if ("multiple".equals(categoria) && paquete.getClass().getSimpleName().equals("PaqueteTuristicoMultiple")) {
                return true;
            }
        }
        return false;
    }

